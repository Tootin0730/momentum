
//Make calculator//

const calculator = {
  add: function (a, b) {
    console.log(a + b);
  },
  minus: function (a, b) {
    console.log(a - b);
  },
  divide: function (a, b) {
    console.log(a / b);
  },
  multi: function (a, b) {
    console.log(a * b);
  },
  power: function (a, b) {
    console.log(a ** b);
  },
};

calculator.add(3, 5);
calculator.minus(3, 5);
calculator.divide(3, 5);
calculator.multi(3, 5);
calculator.power(3, 5);














